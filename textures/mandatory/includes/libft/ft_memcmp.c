#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t num)
{
	if (!s1 || !s2)
	{
		ft_putendl_fd("ft_memcmp() error: null argument", 2);
		return (0);
	}
	while (num--)
	{
		if (*(unsigned char *)s1 != *(unsigned char *)s2)
			return (*(unsigned char *)s1 - *(unsigned char *)s2);
		s1++;
		s2++;
	}
	return (0);
}
